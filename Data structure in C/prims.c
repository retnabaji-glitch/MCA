#include <stdio.h>
#define MAX 30
int parent[MAX];
int find(int i) {
while (parent[i])
i = parent[i];
return i;
}
int uni(int i, int j) {
if (i != j) {
parent[j] = i;
return 1;
}
return 0;
}
int main() {
int cost[MAX][MAX], n, i, j, a, b, u, v, min, ne = 1, mincost = 0;
printf("Enter number of vertices: ");
scanf("%d", &n);
printf("Enter cost adjacency matrix:\n");
for (i = 1; i <= n; i++)
for (j = 1; j <= n; j++) {
scanf("%d", &cost[i][j]);
if (cost[i][j] == 0)
cost[i][j] = 999;
}
printf("Edges of Minimum Cost Spanning Tree:\n");
while (ne < n) {
for (i = 1, min = 999; i <= n; i++)
for (j = 1; j <= n; j++)
if (cost[i][j] < min) {
min = cost[i][j];
a = u = i;
b = v = j;
}
u = find(u);
v = find(v);
if (uni(u, v)) {
printf("%d edge (%d,%d) = %d\n", ne++, a, b, min);
mincost += min;
}
cost[a][b] = cost[b][a] = 999;
}

printf("Minimum cost = %d\n", mincost);
return 0;
}
